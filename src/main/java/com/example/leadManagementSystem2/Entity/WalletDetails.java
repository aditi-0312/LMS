package com.example.leadManagementSystem2.Entity;

import java.sql.Date;

import org.hibernate.annotations.CreationTimestamp;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.OneToOne;


@Entity
public class WalletDetails {

	
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Long id;

	@CreationTimestamp
	private Date date;
	
	private String transaction_id;
	
	private Long amount = 0L;
	
	private String rejection_reason;
	
	private String status;
	
	
	@ManyToOne
	@JoinColumn(name = "businessAssociate_id")
	private BusinessAssociate businessAssociate;
	
	@OneToOne
	@JoinColumn(name = "lead_id")
	private Leads leads;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public String getTransaction_id() {
		return transaction_id;
	}

	public void setTransaction_id(String transaction_id) {
		this.transaction_id = transaction_id;
	}

	public Long getAmount() {
		return amount;
	}

	public void setAmount(Long amount) {
		this.amount = amount;
	}

	public String getRejection_reason() {
		return rejection_reason;
	}

	public void setRejection_reason(String rejection_reason) {
		this.rejection_reason = rejection_reason;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public BusinessAssociate getBusinessAssociate() {
		return businessAssociate;
	}

	public void setBusinessAssociate(BusinessAssociate businessAssociate) {
		this.businessAssociate = businessAssociate;
	}

	public Leads getLeads() {
		return leads;
	}

	public void setLeads(Leads leads) {
		this.leads = leads;
	}

	public WalletDetails() {
		super();
		// TODO Auto-generated constructor stub
	}

	public WalletDetails(Long id, Date date, String transaction_id, Long amount, String rejection_reason, String status,
			BusinessAssociate businessAssociate, Leads leads) {
		super();
		this.id = id;
		this.date = date;
		this.transaction_id = transaction_id;
		this.amount = amount;
		this.rejection_reason = rejection_reason;
		this.status = status;
		this.businessAssociate = businessAssociate;
		this.leads = leads;
	}

	@Override
	public String toString() {
		return "WalletDetails [id=" + id + ", date=" + date + ", transaction_id=" + transaction_id + ", amount="
				+ amount + ", rejection_reason=" + rejection_reason + ", status=" + status + ", businessAssociate="
				+ businessAssociate + ", leads=" + leads + "]";
	}
	
	

	
	
}
